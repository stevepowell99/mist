/** All icon names, for the help reference. */
export const ICON_NAMES = Object.keys(ICONS);

/** Whether a `:name:` shortcode is a known icon. */
export function hasIcon(name: string): boolean {
  return name in ICONS;
}

/** The inline `<svg>` for an icon, sized in `em` and stroked in the current text
 *  colour, so it scales with its context (e.g. the big figure of a .bignums row)
 *  and a colour/theme class recolours it. */
export function renderIcon(name: string): string {
  return `<svg class="icon" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${ICONS[name]}</svg>`;
}
